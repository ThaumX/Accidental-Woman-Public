class Filter {
  // public emptyList: boolean; // Instantiate filter with no blacklist
  public list: string[]; // Instantiate filter with custom list
  public exclude: string[]; // words ignored by filter instance
  public placeHolder: string; // Character used to replace profane words.
  public regex: RegExp; // Regular expression used to sanitize words before comparing them to blacklist.
  public replaceRegex: RegExp; // - Regular expression used to replace profane words with placeHolder.
  constructor({
    emptyList,
    list,
    exclude,
    placeHolder,
    regex,
    replaceRegex,
  }: {
    emptyList?: boolean,
    list?: string[],
    exclude?: string[],
    placeHolder?: string,
    regex?: RegExp,
    replaceRegex?: RegExp,
  }) {
    if (list == null) {
      this.list = clone(aw.badWords);
    } else if (emptyList != null) {
      this.list = clone(list);
    } else {
      this.list = aw.badWords.concat(list);
    }
    if (exclude == null) {
      this.exclude = [];
    } else {
      this.exclude = clone(exclude);
    }
    if (placeHolder == null) {
      this.placeHolder = "█";
    } else {
      this.placeHolder = placeHolder;
    }
    if (regex == null) {
      this.regex = /[^a-zA-Z0-9|\$|\@]|\^/g;
    } else {
      this.regex = regex;
    }
    if (replaceRegex == null) {
      this.replaceRegex = /\w/g;
    } else {
      this.replaceRegex = replaceRegex;
    }
  }

  // Determine if a string contains profane language.
  public isProfane(string: string) {
    return this.list
      .filter((word) => {
        const wordExp = new RegExp(`\\b${word.replace(/(\W)/g, "\\$1")}\\b`, "gi");
        return !this.exclude.includes(word.toLowerCase()) && wordExp.test(string);
      })
      .length > 0 || false;
  }

  // Replace a word with placeHolder characters;
  public replaceWord(string: string) {
    return string
      .replace(this.regex, "")
      .replace(this.replaceRegex, this.placeHolder);
  }

  // Evaluate a string for profanity and return an edited version.
  public clean(string: string) {
    return string.split(/\b/).map((word) => {
      return this.isProfane(word) ? this.replaceWord(word) : word;
    }).join("");
  }

  // Add word(s) to blacklist filter / remove words from whitelist filter
  public addWords(...words) {
    this.list.push(...words);
    words
      .map((word) => word.toLowerCase())
      .forEach((word) => {
        if (this.exclude.includes(word)) {
          this.exclude.splice(this.exclude.indexOf(word), 1);
        }
      });
  }

  // Add words to whitelist filter
  public removeWords(...words) {
    this.exclude.push(...words.map((word) => word.toLowerCase()));
  }
}

